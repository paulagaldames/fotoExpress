package Persona;

public class Persona {
    private String nombre;
    private String apellido;
    private String rut;
    private String telefono;
    private String Correo;

    public Persona(String nombre, String apellido, String rut, String telefono, String Correo) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.rut = rut;
        this.telefono = telefono;
        this.Correo = Correo;
    }
}
